function [ oArgs ] = MatrixN(psi, teta)

N=zeros(6,8);
N(1,1) = N(4,5) = 0.25*(1-psi)*(1+teta);
N(1,2) = N(4,6) = 0.25*(1+psi)*(1+teta);
N(1,3) = N(4,7) = 0.25*(1+psi)*(1-teta);
N(1,4) = N(4,8) = 0.25*(1-psi)*(1-teta);

N(2,1) = N(5,5) = -0.25*(1+teta);
N(2,2) = N(5,6) = 0.25*(1+teta);
N(2,3) = N(5,7) = 0.25*(1-teta);
N(2,4) = N(5,8) = -0.25*(1-teta);

N(3,1) = N(6,5) = 0.25*(1-psi);
N(3,2) = N(6,6) = 0.25*(1+psi);
N(3,3) = N(6,7) = -0.25*(1+psi);
N(3,4) = N(6,8) = -0.25*(1-psi);


oArgs = N;

end
